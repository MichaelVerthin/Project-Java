public class expressions {
    public static void main(String[] args) {
        System.out.println(10 / 5); // 2
        System.out.println(10 % 5); // 0
        System.out.println(26 / 5); // 5
        System.out.println(26 % 5); // 1
        System.out.println(9.0 / 2); // 4.5

        System.out.println(10 / 3); // 3
        System.out.println(10 % 3); // 1
        System.out.println(100 / 9); // 11
        System.out.println(100 % 9); // 1
        System.out.println(987 % 10); // 7
        System.out.println(987 / 10 % 10); // 8
        System.out.println(987 / 100); // 9
        System.out.println(987.0 / 100); // 9.87
    }
}
